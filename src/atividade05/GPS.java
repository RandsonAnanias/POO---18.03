package atividade05;

public class GPS {
	private String Idioma;
	private String Rota;
	
	GPS(){}
	public void setIdioma (String Idioma) {
		this.Idioma = Idioma;
	}
	public void setRota (String Rota) {
		this.Rota = Rota;
	}
	public String getIdioma() {
		return this.Idioma;
	}
	public String getRota() {
		return this.Rota;
	}
}
